function arrowKeys() {
  let menu = false;
  let countX = 0;
  let countY = 0;
  document.onkeydown = (e) => {
    switch (e.which) {
      case 13:
        film(
          document.querySelector(".movies").querySelectorAll("div")[countY]
            .children[countX].className
        );
      case 37:
        if (countX > 0) {
          countX--;
          moveLeft(countX, countY);
        } else {
          moveMenu(countX);
          menu = true;
        }
        break;
      case 38:
        if (countY > 0) {
          countX = 0;
          countY--;
          moveTop(countX, countY);
        } else {
          countX--;
          moveMenuTop(countX);
        }
        break;
      case 39:
        if (menu) {
          menu = false;
          moveMenuExit(countX);
          countX = 0;
        } else {
          countX++;
          moveRight(countX, countY);
        }
        break;
      case 40:
        if (menu) {
          countX++;
          moveMenuBottom(countX);
        } else {
          countX = 0;
          countY++;
          moveBottom(countX, countY);
        }
        break;
      default:
        return;
    }
    e.preventDefault();
  };
}

